package edu.zcs.com.educationsys.util.entity;

public class Account {
	private String aid;
	private String num;
	private String account;
	private String apassword;
	private String aname;
	private String aphonenumber;
	private String aaddress;
	private String type;
	private String ahead;
	private String sex;
	private String area;
	public String getAid() {
		return aid;
	}
	public void setAid(String aid) {
		this.aid = aid;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getApassword() {
		return apassword;
	}
	public void setApassword(String apassword) {
		this.apassword = apassword;
	}
	public String getAname() {
		return aname;
	}
	public void setAname(String aname) {
		this.aname = aname;
	}
	public String getAphonenumber() {
		return aphonenumber;
	}
	public void setAphonenumber(String aphonenumber) {
		this.aphonenumber = aphonenumber;
	}
	public String getAaddress() {
		return aaddress;
	}
	public void setAaddress(String aaddress) {
		this.aaddress = aaddress;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getAhead() {
		return ahead;
	}
	public void setAhead(String ahead) {
		this.ahead = ahead;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}
}
